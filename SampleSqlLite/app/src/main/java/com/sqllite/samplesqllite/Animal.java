package com.sqllite.samplesqllite;

public class Animal {
	
	private int id;
	private String animalName;
	private String animalTallness;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAnimalName() {
		return animalName;
	}
	public void setAnimalName(String animalName) {
		this.animalName = animalName;
	}
	public String getAnimalTallness() {
		return animalTallness;
	}
	public void setAnimalTallness(String animalTallness) {
		this.animalTallness = animalTallness;
	}
	
	

}
